void PWM_set_Freq_DutyCycle(uint16_t freq, uint8_t duty, uint32_t tiempo){
	uint16_t valor_CCR;

	if(duty > 100) duty = 100;
	freq = HAL_RCC_GetHCLKFreq()/(freq*(htim11.Instance->PSC + 1));//le sumo +1 porque aqui no es la formaula normal de (psc+1)x(ARR+1) sino es mi formula
    HAL_TIM_PWM_Start(&htim11, TIM_CHANNEL_1);
	__HAL_TIM_SET_AUTORELOAD(&htim11,freq);//Si quiero cambiar la frecuencia modifico: __HAL_TIM_SET_AUTORELOAD(&htim15, frecuencia que quiero)
	valor_CCR = (__HAL_TIM_GET_AUTORELOAD(&htim11)+1)*duty/100;//__HAL_TIM_GET_AUTORELOAD(&htim15): esto es el valor de 10-1 osea 9, por eso le sumo 1 para volver a tener 10, y por 0.5 ya que quiero 50% del duty cycle
    __HAL_TIM_SET_COMPARE(&htim11, TIM_CHANNEL_1, valor_CCR);
    HAL_Delay(tiempo);
    //HAL_TIM_PWM_Stop(&htim14, TIM_CHANNEL_1);
}
